package com.flipabit.apple.music.model.attribute;

public class EditorialNotes
{
    private String shortNotes;

    private String standardNotes;

    public String getShort()
    {
        return shortNotes;
    }

    public void setShort(String shortNotes)
    {
        this.shortNotes = shortNotes;
    }

    public String getStandard()
    {
        return standardNotes;
    }

    public void setStandard(String standardNotes)
    {
        this.standardNotes = standardNotes;
    }
}
